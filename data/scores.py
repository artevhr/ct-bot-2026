def primary_to_test(subject_key: str, primary: int) -> int:
    table = SUBJECTS[subject_key]["table"]
    primary = max(0, min(primary, SUBJECTS[subject_key]["max_primary"]))
    return table.get(primary, 0)

def get_grade(test_score: int) -> str:
    if test_score >= 90: return "🏆 Отлично (90–100)"
    if test_score >= 75: return "🥇 Хорошо (75–89)"
    if test_score >= 55: return "✅ Удовлетворительно (55–74)"
    if test_score >= 35: return "⚠️ Ниже среднего (35–54)"
    return "❌ Слабо (0–34)"

def format_score_result(subject_key: str, primary: int) -> str:
    subj = SUBJECTS[subject_key]
    test = primary_to_test(subject_key, primary)
    grade = get_grade(test)
    pct = round(primary / subj["max_primary"] * 100)
    return (
        f"📊 *Результат — {subj['name']}*\n\n"
        f"▪️ Первичный балл: *{primary}* из {subj['max_primary']}\n"
        f"▪️ Тестовый балл: *{test}* из 100\n"
        f"▪️ Процент: {pct}%\n\n"
        f"{grade}"
    )
