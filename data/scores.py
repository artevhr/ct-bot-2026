    if p <= 5:
        ENG_TABLE[p] = p * 2
    elif p <= 10:
        ENG_TABLE[p] = 10 + (p - 5) * 3
    elif p <= 20:
        ENG_TABLE[p] = 25 + (p - 10) * 3
    elif p <= 30:
        ENG_TABLE[p] = 55 + (p - 20) * 2
